import time
def display_start_game_details(player1, player2, total_rounds):
    print()
    print("Starting game...")
    time.sleep(1)
    print(f"Player 1: {player1.name}")
    time.sleep(1)
    print(f"Player 2: {player2.name}")
    time.sleep(1)
    print(f"Total rounds: {total_rounds}")
    time.sleep(1)